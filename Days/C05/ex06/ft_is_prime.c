int	ft_is_prime(int nb)
{
	int		i;

	i = 2;
	if (nb == 2147483647)
		return (1);
	while (i < (nb / 3))
	{
		if ((nb % i) == 0)
			return (0);
		i++;
	}
	return (1);
}
