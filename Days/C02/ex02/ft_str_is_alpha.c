int	ft_str_is_alpha(char *str)
{
	int		i;
	int		x;

	i = 0;
	x = 1;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			x = 1;
		else if (str[i] >= 'a' && str[i] <= 'z')
			x = 1;
		else
			return (0);
		i++;
	}
	return (x);
}
