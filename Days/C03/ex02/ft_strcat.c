char	*ft_strcat(char *dest, char *src)
{
	int		i;
	int		x;

	i = 0;
	x = 0;
	while (dest[i] != '\0')
		i++;
	while (src[x] != '\0')
		dest[i++] = src[x++];
	dest[i] = '\0';
	return (dest);
}
